                        the spread of the Gospel, the youths started the meeting with heartfelt prayers and moments of worship. Several special presentations from the delegates and a ‘Bible 
                        Man’ gameshow, among others, made the session lively and exciting. The delegates were delighted to have as their instructor, the Director of the BLW Campus Ministry, 
                        Pastor Freedom-Wealth Eriya. In a rousing message, he charged the youths to always be conscious of fulfilling Christ’s purpose in them. He enumerated their purpose in 
                        Christ as reflecting the Father’s glory, preaching the Gospel and establishing the Kingdom of God in all nations.
