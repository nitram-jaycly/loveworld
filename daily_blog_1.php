                        The next day was filled with wonderful activities and visits to the Lion & Rhino Park and Gold Reef City, the delegates arrived at the evening session with so 
                        much enthusiasm for a time of spiritual enlightenment and insight. Sharing the impact of the camp thus far, Barbara from Switzerland said, “My experience at the 
                        Youth Camp has been great and full of glory. The teachings have been amazing, my heart has been touched. Thank you Pastor Chris, for putting the Word in us.”
                    </p>
                    <p>
                        The message for the day was delivered by the Global Youth Leaders’ Forum Coordinator, Pastor Rhoda Olusina. She talked to the youths about their calling to fulfill 
                        God’s dream, which is for all men to be saved. She urged them to use every opportunity to propagate the Gospel, saying, “You are the preacher to your world. God’s 
                        desire is for all men to be saved; you have been called to fulfill God’s dream. You’re the only one who can reach out to the people in your world. Don’t keep quiet 
                        — all the Holy Spirit wants is for you to open your mouth and let Him fill it with life-giving words.”
                    </p>
