                        “Let your life be for the gospel; fight for it. God is raising soldiers, who will sacrifice their life for it and will not accept anything but the Lordship of Jesus in 
                        their locality. You are His weapon of war. When you get back, fight that the name of Jesus should be know where you are. Fight for the Gospel.”
                    </p>
