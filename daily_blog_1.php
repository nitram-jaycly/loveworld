                        The first night’s session began with moments of worship, along with many special presentations in music and dance from representatives of various countries. 
                        These were capped by a session with the highly esteemed Director of Church Ministry, Rev. Tom Amenkhienan, welcoming the youth to the program and encouraging 
                        them saying, “there’s something special about you being here, because you’re special. You’re going to have an impactful, life-transforming experience. Open 
                        your spirit and receive everything that God is depositing inside you and you’ll turn out champions for life.”
                    </p>
                    <p>
                        Reverend Tom went on to exhort the youths with a message titled, ‘Your Destiny in God’. He explained that there are two kinds of destinies: the natural destiny, 
                        which is controlled by environment, economy and other such factors; and, the divine destiny, which a person enters into once they become born again. “Your destiny 
                        will determine your destination,” he said, expatiating that divine destiny entails unveiling God’s love, His Word and His glory to all men. “You’re God’s hope, 
                        His voice and His medium of expression in your world; that’s why you can’t keep quiet,” he admonished the youths.
                    </p>
