                    </p>
                    <p>
                        The expert panel’s 66-page report to the Security Council, obtained Monday by The Associated Press, also detailed the appearance in North Korea 
                        of a Rolls-Royce Phantom, Mercedes-Benz limousines and Lexus LX 570 all-wheel drive luxury vehicles in violation of a ban on luxury goods.
                        And it noted a trend in North Korea’s evasion of financial sanctions “of using cyberattacks to illegally force the transfer of funds from financial 
                        institutions and cryptocurrency exchanges.”
                    </p>    
                    <p>    
                        The report’s executive summary, which was obtained in early February, said North Korea’s nuclear and missile programs “remain intact” 
                        and its leaders are dispersing missile assembly and testing facilities to prevent “decapitation” strikes.
                    </p>
                    <p>
                        The full report said “the Yongbyon nuclear complex remained active,” noting that satellite imagery through November showed excavation of 
                        water channels and construction of a new building near the reactors’ water discharge facilities. Satellite imagery also “indicates possible 
                        operation of the radiochemical laboratory and associated steam plant,” it said.
                        The panel said it continues monitoring uranium concentration plants and mining sites in the country.
                    </p>
                    <p>
                        It also has “surveyed, confirmed and reported ballistic missile activity sites and found evidence of a consistent trend” 
                        by North Korea “to disperse its assembly, storage and testing locations,” the report said.In addition to using civilian 
                        facilities, the panel said North Korea is using “previously idle or sprawling military-industrial sites as launch locations” 
                        — some close to, and some up to 10 kilometers (6 miles) from the assembly or storage sites.
                    </p>
                    <p>
                        As examples of this trend, it cited the test launch of Hwasong-14 intercontinental ballistic missiles from the Panghyon aircraft 
                        factory on July 4, 2017, and a launch from Mupyong-ni 24 days after that. It said Pyongyang’s Sunan International Airport, the 
                        country’s largest civil-military airfield, was used to launch Hwasong-12 missiles on Aug. 29 and Sept. 15 of that year.
                    </p>
                    <p>
                        As for trade sanctions, the experts said they continue to investigate two Chinese companies on the U.N. sanctions blacklist — Namchogang 
                        Trading Corp. and Namhung Trading Corp. — and associated front companies and their representatives “for nuclear procurement activities.”
                        The panel said it is also currently surveying the world’s manufacturers of nuclear “choke point” items such as “pressure transducers,” 
                        focusing on their end-use delivery verification methods.
                    </p>
                    <p>
                        The experts said they also were continuing “multiple investigations into prohibited activities” between North Korea and the Syrian government 
                        of President Bashar Assad. These include Syrian nationals reported to be engaged in arms brokering on behalf of North Korea “to a range of Middle 
                        Eastern and African states, reportedly offering conventional arms and, in some cases, ballistic missiles, to armed groups in Yemen and Libya,” the 
                        panel said. They also include North Koreans working for sanctioned “entities” and for Syrian defense factories, it said.
                    </p>
                    <p>
                        The experts said a country, which they didn’t identify, had informed them that Iran “was one of the two most lucrative markets” for North 
                        Korean military cooperation and that both the Korea Mining Development Trading Corp. and Green Pine Associated Corp. offices in the country 
                        “are active.” The unnamed country also indicated that North Koreans in Iran were being used as cash couriers, the report said.
                        The Iranian government replied to the panel that the only North Koreans in the country were diplomats, and they have not violated 
                        U.N. sanctions, the report said.
                    </p>
