                    <p>
                        He is convinced the online population will continue to grow, but says accessibility issues continue to beset much of the world.
                        “Look at the 50 percent who are on the web, and it’s not so pretty for them,” he said. “They are all stepping back suddenly 
                        horrified after the Trump and Brexit elections realizing that this web thing that they thought was so cool has actually not 
                        necessarily been serving humanity very well.”The anniversary is also a nod to the innovative, collaborative and open-source 
                        mindset at the Geneva-based CERN, where physicists smash particles together to unlock secrets of science and the universe.
                        As a young English software engineer, Berners-Lee came up with the idea for hypertext-transfer protocol — the “http” that adorns 
                        web addresses — and other building blocks for the web while working at CERN in March 1989. Some trace the actual start of the web 
                        to 1990, when he released the first web browser.
                    </p>
                    <p>
                        Berners-Lee reminisced about how he was really out to get disparate computer systems to talk to one another, and resolve the “burning 
                        frustration” over a “lack of interoperability” of documentation from disparate computing systems used at CERN in the late 1980s. Now, 
                        the hope of his World Wide Web Foundation is to enlist governments, companies, and citizens to take a greater role in shaping the web 
                        for good under principles laid out in its “Contract for the Web.” Under the contract’s sweeping, broad ambition, governments are supposed 
                        to make sure everyone can connect to the internet, to keep it available and to respect privacy. Companies are to make the internet affordable, 
                        respect privacy and develop technology that will put people — and the “public good” — first. Citizens are to create and to cooperate and 
                        respect “civil discourse,” among other things. To Berners-Lee, the web is a “mirror of humanity” where “you will see good and bad.” 
                    </p>
                    <p>
                        “The Contract for the Web recognizes that whether humanity, in fact, is constructive or not actually depends on the way you write the code 
                        of the social network,” he said. Some tough regulation may be necessary in some places, in others not, Berners-Lee said. On one issue, he’s 
                        insistent: “Net neutrality — strong regulation,” Berners-Lee said, hammering a fist on the table. He was alluding to a principle that anyone 
                        with an internet connection should have equal access to video, music, email, photos, social networks, maps and other online material.
                        Berners-Lee said the web has created opportunity, made lives easier and given the marginalized a voice, but “it has also created opportunity 
                        for scammers, given a voice to those who spread hatred, and made all kinds of crime easier to commit.”
                    </p>
